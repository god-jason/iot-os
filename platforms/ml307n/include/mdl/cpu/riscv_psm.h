/************************************************************************************
 *                                 宏定义
 ************************************************************************************/
#define RISCV_ENTER_WFI              do{asm volatile("fence iorw, iorw\n" "nop\n" "wfi\n");}while(0)
#define RISCV_SET_BARRIER            do{asm volatile("fence iorw, iorw\n");}while(0)
#define RISCV_BPU_ENABLE             do{asm volatile("csrs 0x7d0, 0x8\n");}while(0)

