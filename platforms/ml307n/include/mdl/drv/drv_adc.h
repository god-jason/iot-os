#ifndef _DRV_ADC_H
#define _DRV_ADC_H

/**
 * @addtogroup Adc
 */

/**@{*/

/************************************************************************************
 *                                 头文件
 ************************************************************************************/
#include <drv_common.h>

/************************************************************************************
 *                                 配置开关
 ************************************************************************************/


/************************************************************************************
 *                                 宏定义
 ************************************************************************************/
/**
 * @brief 函数返回值
 */
#define         ADC_OK                  DRV_OK
#define         ADC_ERROR               (DRV_ERR_SPECIFIC - 1)
#define         ADC_TIME_DELAY_ERROR    (DRV_ERR_SPECIFIC - 2)

/************************************************************************************
 *                                 类型定义
 ************************************************************************************/
typedef enum
{
    ADC_CH_PA_TEMP = 0,
    ADC_CH_XO_TEMP = 1,     //ADC1
    ADC_CH_USER_DEFINE0 = 2,//ADC2
    ADC_CH_USER_DEFINE1 = 3,//ADC3
    ADC_CH_VBAT = 4,
    ADC_CH_RF_TEMP = 5,
    ADC_CH_MAX
}ADC_Chnl;

typedef struct
{
    uint8_t chnl[ADC_CH_MAX];
    uint8_t chnlNum;
}ADC_Config;

/************************************************************************************
 *                                 函数定义
 ************************************************************************************/
/**
 ************************************************************************************
 * @brief           读取ADC 原始数据
 *
 * @param[in]       ADC 通道号
 *
 * @return          .
 * @retval          > 0                         AD 值
 *                  < 0                         失败
 ************************************************************************************
 */
int32_t ADC_ReadRawData(ADC_Chnl chnl);

/**
 ************************************************************************************
 * @brief           读取ADC 转换后的电压值
 *
 * @param[in]       ADC 通道号
 *
 * @return          .
 * @retval          > 0                         电压值，单位mv
 *                  < 0                         失败
 ************************************************************************************
 */
int32_t ADC_ReadVoltData(ADC_Chnl chnl);

/**
 ************************************************************************************
 * @brief           读取测量温度的通道
 *
 * @param[in]       ADC 通道号
 * @param[in]       temp 温度
 *
 * @return          .
 * @retval          >= 0                        succ
 *                  < 0                         失败
 ************************************************************************************
 */
int32_t ADC_ReadTemp(ADC_Chnl chnl, int32_t *temp);

/**
 ************************************************************************************
 * @brief           ADC上电清共享内存
 *
 * @return          .
 * @retval          > 0                         温度
 *                  < 0                         失败
 ************************************************************************************
 */
int ADC_Initialize(void);

/**
 ************************************************************************************
 * @brief           ADC时是否准备好
 *
 * @return          .
 * @retval          > 0                         温度
 *                  < 0                         失败
 ************************************************************************************
 */
bool ADC_IsReady(void);
#endif

/** @} */