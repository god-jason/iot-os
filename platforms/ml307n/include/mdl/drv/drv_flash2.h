#ifndef __DRIVER_FLASH2_H__
#define __DRIVER_FLASH2_H__

/************************************************************************************
 *                                 头文件
 ************************************************************************************/

#include "drv_flash.h"

/**
 * @addtogroup Flash
 */

/**@{*/

/************************************************************************************
 *                                 宏定义
 ************************************************************************************/

/************************************************************************************
 *                                 函数定义
 ************************************************************************************/

/**
 ************************************************************************************************************************
 * @brief       获取Nor2Flash.
 * @param[in]   void
 * @return      FLASH_Nor *.
 * @retval      !OS_NULL         Nor2Flash.
 ************************************************************************************************************************
 */
FLASH_Nor *SPI_GetSpiNor2(void);

/**
 ************************************************************************************************************************
 * @brief       从Nor2Flash读数据.
 * @param[in]   nor              Nor2Flash.
 * @param[in]   from             待读数据在NorFlash内的偏移地址.
 * @param[in]   len              待读数据长度.
 * @param[in]   buf              待读数据存放的buffer.
 * @return      int32_t.
 * @retval      >= 0             成功读取字节个数.
 * @retval      < 0              读取错误返回类型.
 ************************************************************************************************************************
 */
int32_t SPI_Nor2Read(FLASH_Nor *nor, uint32_t from, uint32_t len, uint8_t *buf);

/**
 ************************************************************************************************************************
 * @brief       写数据到Nor2Flash.
 * @param[in]   nor              Nor2Flash.
 * @param[in]   to               待写数据到NorFlash内的偏移地址.
 * @param[in]   len              待写数据长度.
 * @param[in]   buf              待写数据存放的buffer.
 * @return      int32_t.
 * @retval      >= 0             成功写入字节个数.
 * @retval      < 0              写入错误返回类型.
 ************************************************************************************************************************
 */
int32_t SPI_Nor2Write(FLASH_Nor *nor, uint32_t to, uint32_t len, const uint8_t *buf);

/**
 ************************************************************************************************************************
 * @brief       Nor2Flash 擦除.
 * @param[in]   nor              Nor2Flash.
 * @param[in]   offset           待擦除区域在Flash内的偏移地址.
 * @param[in]   len              待擦除区域长度.
 * @return      int32_t.
 * @retval      OS_OK            成功.
 * @retval      < 0              失败.
 ************************************************************************************************************************
 */
int32_t SPI_Nor2Erase(FLASH_Nor *nor, uint32_t offset, uint32_t len);

/**
 ************************************************************************************************************************
 * @brief       Nor2Flash初始化.
 * @param[in]   nor              Nor2Flash.
 * @return      int32_t.
 * @retval      DRV_OK           成功.
 * @retval      < 0              失败.
 ************************************************************************************************************************
 */
int32_t SPI_Nor2Init(FLASH_Nor *nor);
#endif

/** @} */