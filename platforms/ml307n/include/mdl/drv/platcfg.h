#ifndef __PLAT_CONFIG_H__
#define __PLAT_CONFIG_H__

#include "stdbool.h"

/**
 * @brief 配置开关定义.
 */
typedef enum {
    PLATCFG_FLAG_OFF    = 0x6D,
    PLATCFG_FLAG_ON     = 0xB2,
}PlatCfgFlag;

enum {
    PLATCFG_RRC_OFF    = 0,
    PLATCFG_RRC_ON     = 1,
};

/**
 ************************************************************************************
 * @brief       设置mux开关.
 ************************************************************************************
 */
int32_t PlatCfg_SetMuxFlag(uint8_t flag);

/**
 ************************************************************************************
 * @brief       获取mux开关.
 ************************************************************************************
 */
uint8_t PlatCfg_GetMuxFlag(void);

/**
 ************************************************************************************
 * @brief       设置开门狗开关.
 ************************************************************************************
 */
int32_t     PlatCfg_SetWatchdogFlag(uint8_t flag);

/**
 ************************************************************************************
 * @brief       获取开门狗开关.
 ************************************************************************************
 */
int32_t PlatCfg_GetWatchdogFlag(void);

/**
 ************************************************************************************
 * @brief       设置开门狗开关.
 ************************************************************************************
 */
int32_t PlatCfg_SetUiccFlag(uint8_t val);

/**
 ************************************************************************************
 * @brief       获取开门狗开关.
 ************************************************************************************
 */
uint8_t PlatCfg_GetUiccgFlag(void);

/**
 ************************************************************************************
 * @brief       设置异常重启开关.
 ************************************************************************************
 */
int32_t     PlatCfg_SetExcepResetFlag(PlatCfgFlag flag);

/**
 ************************************************************************************
 * @brief       获取异常重启开关.
 ************************************************************************************
 */
PlatCfgFlag PlatCfg_GetExcepResetFlag(void);

/**
 ************************************************************************************
 * @brief       设置省电模式等级的NV.
 ************************************************************************************
 */
int32_t PlatCfg_SetPsmLevel(uint8_t level);

/**
 ************************************************************************************
 * @brief       获取省电模式等级.
 ************************************************************************************
 */
int32_t PlatCfg_GetPsmLevel(void);

/**
 ************************************************************************************
 * @brief       设置PSM唤醒保持时长.
 ************************************************************************************
 */
int32_t PlatCfg_SetPsmWakeupTime(uint8_t len);

/**
 ************************************************************************************
 * @brief       设置短按唤醒时长.
 ************************************************************************************
 */
int32_t PlatCfg_SetSpWakeupTime(uint16_t len);

/**
 ************************************************************************************
 * @brief       获取短按唤醒时长.
 ************************************************************************************
 */
int32_t PlatCfg_GetSpWakeupTime(void);

/**
 ************************************************************************************
 * @brief       设置DeepSleep功耗优化开关.
 ************************************************************************************
 */
int32_t PlatCfg_SetDsOptimizeEn(uint8_t en);

/**
 ************************************************************************************
 * @brief       获取DeepSleep功耗优化开关
 ************************************************************************************
 */
int32_t PlatCfg_GetDsOptimizeEn(void);

/**
 ************************************************************************************
 * @brief       设置deepsleep下DCXO26M时钟开关
 ************************************************************************************
 */
int32_t PlatCfg_SetDs26MDcxoEn(uint8_t en);

/**
 ************************************************************************************
 * @brief       获取deepsleep下DCXO26M时钟开关
 ************************************************************************************
 */
int32_t PlatCfg_GetDs26MDcxoEn(void);

/**
 ************************************************************************************
 * @brief       设置sby门限时长.
 ************************************************************************************
 */
int32_t PlatCfg_SetSbyThreholdTime(uint16_t len);

/**
 ************************************************************************************
 * @brief       获取sby门限时长
 ************************************************************************************
 */
int32_t PlatCfg_GetSbyThreholdTime(void);

/**
 ************************************************************************************
 * @brief       设置自适应后获取波特率切换工作时钟的开关
 ************************************************************************************
 */
int32_t PlatCfg_SetAdaptChCLKEn(uint8_t en);

/**
 ************************************************************************************
 * @brief       获取自适应后获取波特率切换工作时钟的开关
 ************************************************************************************
 */
int32_t PlatCfg_GetAdaptChCLKEn(void);

/**
 ************************************************************************************
 * @brief       获取制造商名称
 ************************************************************************************
 */
const char* PlatCfg_GetManufacturer(char *buf, uint32_t len);

/**
 ************************************************************************************
 * @brief       获取产品名称
 ************************************************************************************
 */
const char* PlatCfg_GetDeviceModel(char *buf, uint32_t len);

/**
 ************************************************************************************
 * @brief       获取设备硬件版本号
 ************************************************************************************
 */
const char* PlatCfg_GetHardVersion(char *buf, uint32_t len);

/**
 ************************************************************************************
 * @brief       获取软件版本号
 ************************************************************************************
 */
const char* PlatCfg_GetSoftVersion(char *buf, uint32_t len);

/**
 ************************************************************************************
 * @brief       获取mux开关.
 ************************************************************************************
 */
uint8_t PlatCfg_GetMuxFlag(void);

/**
 ************************************************************************************
 * @brief       获取开机自动cfun=1的标记

 * @param[out]       flag         范围true和false
 *
 * @return  osOK 成功  其他失败
 ************************************************************************************
 */
int PlatCfg_GetAutoCfun1Flag(bool* flag);

/**
 ************************************************************************************
 * @brief       设置开机自动cfun=1的标记

 * @param[in]       flag         范围true和false
 *
 * @return  osOK 成功  其他失败
 ************************************************************************************
 */
int PlatCfg_SetAutoCfun1Flag(bool flag);

/**
 ************************************************************************************
 * @brief       获取开机自动连接的Cid值

 * @param[out]       cid         PDP ID  取值范围0-8，0表示不需要自动连接
 *
 * @return  osOK 成功  其他失败
 ************************************************************************************
 */
int PlatCfg_GetAutoConnectCid(uint8_t* cid);

/**
 ************************************************************************************
 * @brief       设置开机自动连接的Cid值

 * @param[in]       cid         PDP ID  取值范围0-8，0表示不需要自动连接
 *
 * @return  osOK 成功  其他失败
 ************************************************************************************
 */
int PlatCfg_SetAutoConnectCid(uint8_t cid);

/**
 ************************************************************************************
 * @brief       AT保存波特率到NV中
 ************************************************************************************
 */
int32_t PlatCfg_ATBaud(uint32_t arg);

/**
 ************************************************************************************
 * @brief       9600时钟切换保存NV（32K、26M）
 ************************************************************************************
 */
int32_t PlatCfg_ATSelectClock(uint32_t arg);

/**
 ************************************************************************************
 * @brief       获取9600时钟配置NV
 ************************************************************************************
 */
int32_t PlatCfg_ATGetClock(void);

/**
 ************************************************************************************
 * @brief       设置CONSOLE开关.
 ************************************************************************************
 */
int32_t PlatCfg_SetConsoleCtrlFlag(uint8_t flag);

/**
 ************************************************************************************
 * @brief       获取CONSOLE开关.
 ************************************************************************************
 */
int32_t PlatCfg_GetConsoleCtrlFlag(void);

/**
 ************************************************************************************
 * @brief       设置 RRC 超时拆链开关.
 ************************************************************************************
 */
int32_t PlatCfg_SetFastReleaseRrcCtrlFlag(uint8_t flag);

/**
 ************************************************************************************
 * @brief       获取 RRC 超时拆链开关.
 ************************************************************************************
 */
int32_t PlatCfg_GetFastReleaseRrcCtrlFlag(void);

#endif
