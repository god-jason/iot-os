#ifndef  __PSM_WAKELOCK__
#define  __PSM_WAKELOCK__
/************************************************************************************
 *                                 头文件定义
 ************************************************************************************/
#include <os_list.h>
#include <os_def.h>
#include <psm_core.h>

/**
 * @addtogroup WakeLock
 */

/**@{*/

/************************************************************************************
 *                                 宏定义
 ************************************************************************************/


/************************************************************************************
 *                                 类型定义
 ************************************************************************************/
/**
\brief: wakelock info.
*/
typedef struct {
    osList_t node;
    uint16_t refCount;
    uint16_t flag;
} WakeLock;


/************************************************************************************
 *                                 函数定义
 ************************************************************************************/
/**
 ************************************************************************************
 * @brief           初始化Wakelock
 *
 * @param[in]       wakelock    Wakelock指针
 * @param[in]       mode        低功耗模式
 *
 * @return          void
 * @retval          null
 ************************************************************************************
*/
void PSM_WakelockInit(WakeLock *wakelock, PSM_Mode mode);

/**
 ************************************************************************************
 * @brief           启动Wakelock
 *
 * @param[in]       wake_lock    Wakelock指针
 *
 * @return          void
 * @retval          void
 ************************************************************************************
*/
void PSM_WakeLock(WakeLock *wakelock);

/**
 ************************************************************************************
 * @brief           停止Wakelock
 *
 * @param[in]       wake_lock    Wakelock指针
 *
 * @return          void
 * @retval          void
 ************************************************************************************
*/
void PSM_WakeUnlock(WakeLock *wakelock);

/**
 ************************************************************************************
 * @brief           检查系统中是否有Wakelock
 *
 * @param[in]       void
 *
 * @return          PSM_Mode
 ************************************************************************************
*/
PSM_Mode PSM_WakelockCheck(void);

#endif
/** @} */